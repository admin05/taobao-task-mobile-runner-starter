function resolveTargets(arg) {
  if (!arg) return [TARGETS["baba-farm"]];
  if (arg === "all") return Object.values(TARGETS);
  if (!TARGETS[arg]) {
    const names = Object.keys(TARGETS).join(", ");
    throw new Error(`Unknown target "${arg}". Expected one of: ${names}, all`);
  }
  return [TARGETS[arg]];
}

function buildTarget(target) {
  const config = JSON.parse(fs.readFileSync(target.configPath, "utf8"));
  const { urls, waitSeconds } = buildTaskUrls(config);
  if (!urls.length) throw new Error("No task URLs generated");
  const xml = buildShortcutXml(target, urls, waitSeconds);
  assertLocalOnlyXml(xml);
  fs.mkdirSync(path.dirname(target.outputPath), { recursive: true });
  fs.writeFileSync(target.outputPath, xml);
  console.log(`Generated ${target.outputPath}`);
  console.log(`Embedded local task URLs: ${urls.length}`);
  console.log(`Delay seconds: ${waitSeconds}`);
  console.log("Remote JSON actions: none");
}

function main() {
  for (const target of resolveTargets(process.argv[2])) {
    buildTarget(target);
  }
}

main();
