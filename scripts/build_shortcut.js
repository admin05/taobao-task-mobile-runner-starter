function escapeXml(value) {
  return String(value)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&apos;");
}

function pickRandom(items) {
  return items[Math.floor(Math.random() * items.length)];
}

function replaceParams(baseUrl, taskItem, paramObj) {
  let url = baseUrl;
  for (const [key, values] of Object.entries(paramObj || {})) {
    url = url.replace(new RegExp(key, "g"), pickRandom(values));
  }
  String(taskItem).split(" ").forEach((value, idx) => {
    if (!value) return;
    url = url.replace(new RegExp(`占位参数${idx + 1}`, "g"), value);
  });
  return url;
}

function buildTaskUrls(config) {
  const taskNames = config.activity?.taskList || [];
  const urls = [];
  const waits = [];
  for (const taskName of taskNames) {
    const task = config.taskData?.[taskName]?.task;
    if (!task) continue;
    if (Number.isFinite(Number(task.wait))) waits.push(Number(task.wait));
    for (const before of task.before || []) {
      if (Number.isFinite(Number(before.wait))) waits.push(Number(before.wait));
      if (before.url) urls.push(`taobao${replaceParams(before.url, "", task.param)}`);
    }
    for (const item of task.item || []) {
      const baseUrl = replaceParams(task.baseurl, item, task.param);
      urls.push(`taobao${baseUrl}`);
    }
  }
  return {
    urls,
    waitSeconds: waits.length ? Math.max(...waits) : 20,
  };
}

function assertLocalOnlyXml(xml) {
  const remoteConfigMarkers = [
    "is.workflow.actions.downloadurl",
    "bbnc.json",
    "raw.gitcode.com",
  ];
  for (const marker of remoteConfigMarkers) {
    if (xml.includes(marker)) {
      throw new Error(`Generated shortcut unexpectedly references remote configuration: ${marker}`);
    }
  }
}

